

public class Position {
    //Class fields
    private int x;
    private int y;

    //Class constructor
    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    //Class methods
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
